//    Simpan Data Kota
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(txtid_kota.getText().equals("")){
                JOptionPane.showMessageDialog(this, "ID KOTA Belum di Isi");
                txtid_kota.requestFocus();
            } else if(txtnama_kec.getText().equals("")){
                JOptionPane.showMessageDialog(this, "NAMA KECAMATAN Belum di Isi");
                txtnama_kec.requestFocus();
            } else if(txtkota.getText().equals("")){
                JOptionPane.showMessageDialog(this, "NAMA KOTA Belum di Isi");
                txtkota.requestFocus();
            } else if(txtkodepos.getText().equals("")){
                JOptionPane.showMessageDialog(this, "KODE POS Belum di Isi");
                txtkodepos.requestFocus();
            } else if(txtdeskripsi.getText().equals("")){
                JOptionPane.showMessageDialog(this, "DESKRIPSI Belum di Isi");
                txtdeskripsi.requestFocus();
            } else {
                crud.simpandatakota(txtid_kota.getText(), txtnama_kec.getText(), txtkota.getText(), txtkodepos.getText(), txtdeskripsi.getText());
                JOptionPane.showMessageDialog(this, "Yay, DATA KOTA Berhasil Di Simpan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

