//  cari Data KOta
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String idKota = txtid_kota.getText();
        try {
            // Cek apakah ID Kota tidak kosong
            if (!idKota.equals("")) {
                // Panggil metode caridatakota dari objek koneksi
                ResultSet rs = crud.caridatakota(idKota);
                // Periksa apakah hasil pencarian tidak kosong
                if (rs.next()) {
                    // Jika ada hasil, tampilkan informasi dari hasil pencarian
                    String namaKec = rs.getString("nama_kec");
                    String namaKota = rs.getString("kota");
                    String kodePos = rs.getString("kodepos");
                    String deskripsi = rs.getString("deskripsi");
                    // Tampilkan informasi pada antarmuka pengguna
                    txtnama_kec.setText(namaKec);
                    txtkota.setText(namaKota);
                    txtkodepos.setText(kodePos);
                    txtdeskripsi.setText(deskripsi);
                    JOptionPane.showMessageDialog(this, "Yay, Data Kota ditemukan");
                } else {
                    // Jika tidak ada hasil, tampilkan pesan bahwa data tidak ditemukan
                    JOptionPane.showMessageDialog(this, "Yah, Data Kota tidak ditemukan");
                }
            } else {
                // Jika ID Kota kosong, minta pengguna untuk mengisi ID Kota terlebih dahulu
                JOptionPane.showMessageDialog(this, "Masukkan ID Kota terlebih dahulu");
            }
        } catch (Exception e) {
            // Tangani jika terjadi kesalahan saat mencari data
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

