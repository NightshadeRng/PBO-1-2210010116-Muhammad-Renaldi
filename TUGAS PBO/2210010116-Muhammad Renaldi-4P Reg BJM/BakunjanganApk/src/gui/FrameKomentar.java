    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String idKomentar = txtid_comment.getText();
        try {
            // Cek apakah ID Comment tidak kosong
            if (!idKomentar.equals("")) {
                // Panggil metode cariKomentar dari objek koneksi
                ResultSet rs = crud.carikomentar(idKomentar);
                // Periksa apakah hasil pencarian tidak kosong
                if (rs.next()) {
                    // Jika ada hasil, tampilkan informasi dari hasil pencarian
                    String nama = rs.getString("nama");
                    String email = rs.getString("email");
                    String komentar = rs.getString("komentar");
                    String date = rs.getString("date");
                    String art_url = rs.getString("art_url");
                    // Tampilkan informasi pada antarmuka pengguna
                    txtnama.setText(nama);
                    txtemail.setText(email);
                    txtkomentar.setText(komentar);
                    txttanggal.setText(date);
                    txtart_url.setText(art_url);
                    JOptionPane.showMessageDialog(this, "Yay, Data Komentar ditemukan");
                } else {
                    // Jika tidak ada hasil, tampilkan pesan bahwa data tidak ditemukan
                    JOptionPane.showMessageDialog(this, "Yah, Data Komentar tidak ditemukan");
                }
            } else {
                // Jika ID Komentar kosong, minta pengguna untuk mengisi ID Wisata terlebih dahulu
                JOptionPane.showMessageDialog(this, "Masukkan ID Komentar terlebih dahulu");
            }
        } catch (Exception e) {
            // Tangani jika terjadi kesalahan saat mencari data
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

