    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(txtid_comment.getText().equals("")){
                JOptionPane.showMessageDialog(this, "ID COMMENT Belum di Isi");
                txtid_comment.requestFocus();
            } else if(txtnama.getText().equals("")){
                JOptionPane.showMessageDialog(this, "NAMA Belum di Isi");
                txtnama.requestFocus();
            } else if(txtemail.getText().equals("")){
                JOptionPane.showMessageDialog(this, "EMAIL Belum di Isi");
                txtemail.requestFocus();
            } else if(txtkomentar.getText().equals("")){
                JOptionPane.showMessageDialog(this, "KOMENTAR Belum di Isi");
                txtkomentar.requestFocus();
            } else if(txttanggal.getText().equals("")){
                JOptionPane.showMessageDialog(this, "TANGGAL Belum di Isi");
                txttanggal.requestFocus();
            } else if(txtart_url.getText().equals("")){
                JOptionPane.showMessageDialog(this, "ART URL Belum di Isi");
                txtart_url.requestFocus();
            } else {
                crud.simpankomentar(txtid_comment.getText(), txtnama.getText(), txtemail.getText(), txtkomentar.getText(), txttanggal.getText(), txtart_url.getText());
                JOptionPane.showMessageDialog(this, "Yay, KOMENTAR Berhasil Di Simpan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed
