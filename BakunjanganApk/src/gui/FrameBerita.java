    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String idBerita = txtid_berita.getText();
        try {
            // Cek apakah ID Berita tidak kosong
            if (!idBerita.equals("")) {
                // Panggil metode cariBerita dari objek koneksi
                ResultSet rs = crud.cariberita(idBerita);
                // Periksa apakah hasil pencarian tidak kosong
                if (rs.next()) {
                    // Jika ada hasil, tampilkan informasi dari hasil pencarian
                    String tanggalpost = rs.getString("tanggal_post");
                    String gambar = rs.getString("gambar");
                    String judul = rs.getString("judul");
                    String konten = rs.getString("konten");
                    // Tampilkan informasi pada antarmuka pengguna
                    txttanggal_post.setText(tanggalpost);
                    txtgambar.setText(gambar);
                    txtjudul.setText(judul);
                    txtkonten.setText(konten);
                    JOptionPane.showMessageDialog(this, "Yay, Data Berita ditemukan");
                } else {
                    // Jika tidak ada hasil, tampilkan pesan bahwa data tidak ditemukan
                    JOptionPane.showMessageDialog(this, "Yah, Data Berita tidak ditemukan");
                }
            } else {
                // Jika ID Berita kosong, minta pengguna untuk mengisi ID Wisata terlebih dahulu
                JOptionPane.showMessageDialog(this, "Masukkan ID Berita terlebih dahulu");
            }
        } catch (Exception e) {
            // Tangani jika terjadi kesalahan saat mencari data
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

