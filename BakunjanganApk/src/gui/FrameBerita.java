    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(txtid_berita.getText().equals("")){
                JOptionPane.showMessageDialog(this, "ID BERITA Belum di Isi");
                txtid_berita.requestFocus();
            } else if(txttanggal_post.getText().equals("")){
                JOptionPane.showMessageDialog(this, "TANGGAL POST Belum di Isi");
                txttanggal_post.requestFocus();
            } else if(txtgambar.getText().equals("")){
                JOptionPane.showMessageDialog(this, "GAMBAR Belum di Isi");
                txtgambar.requestFocus();
            }  else if(txtjudul.getText().equals("")){
                JOptionPane.showMessageDialog(this, "JUDUL Belum di Isi");
                txtjudul.requestFocus(); 
            } else if(txtkonten.getText().equals("")){
                JOptionPane.showMessageDialog(this, "KONTEN Belum di Isi");
                txtkonten.requestFocus();
            } else {
                crud.simpanberita(txtid_berita.getText(), txttanggal_post.getText(), txtgambar.getText(), txtjudul.getText(), txtkonten.getText());
                JOptionPane.showMessageDialog(this, "Yay, BERITA Berhasil Di Simpan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }                                        
    }//GEN-LAST:event_jButton1ActionPerformed
